int	ft_runtab(char **tab, int *x, int *y)
{
	(*x)++;
	if (tab[*y][*x] == '\0')
	{
		(*y)++;
		*x = 0;
	}
	if (!tab[*y])
	{
		*y = 0;
		*x = 0;
		return (0);
	}
	return (1);
}
