#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*str;
	unsigned int	count;

	if (!s || !f)
		return (NULL);
	if (!(str = (char*)malloc(sizeof(char) * (ft_strlen(s) + 1))))
		return (NULL);
	count = 0;
	while (s[count])
	{
		str[count] = f(count, s[count]);
		count++;
	}
	str[count] = 0;
	return (str);
}
